function mouseClicked() {
	push();
	translate(mouseX+random(-150, 150), mouseY+random(-200,200));
	drawArray(int(random(3)));
	translate(mouseX+random(-250, 250), mouseY+random(-300,300));
	rotate(radians(random(120)));
	drawArray(int(random(3)));
	translate(mouseX+random(-350, 350), mouseY+random(-300,300));
	rotate(radians(random(120)));
	drawArray(int(random(3)));
	translate(mouseX+random(-150, 150), mouseY+random(-200,200));
	rotate(radians(random(120)));
	drawArray(int(random(3)));
	pop();
}

function drawArray(x) {
	if (x == 1){
		for (var i = 0; i < 16; i++) { 
				for (var j = 0; j < 16; j++) { 
				  a = gridarr1[i][j]; 
					  if (a === 1) { 
						fill(245, 140, 35);
					  } else {
						fill(15, 1, 3);
					  }
				  rect((j * 20) + 15, (i * 20) + 15, 15, 15);
				}
		}
	}
	else if (x == 2){
		for (var i = 0; i < 16; i++) { 
				for (var j = 0; j < 16; j++) { 
				  a = gridarr2[i][j]; 
					  if (a === 1) { 
						fill(23, 145, 167);
					  } else {
						fill(53, 243, 53);
					  }
				  rect((j * 15) + 10, (i * 15) + 10, 10, 10);
				}
		}
	}
	else {
		textSize(64);
		fill(255,0,0);
		text('NOPE', 10, 30);
	}
}
